#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {
    // Return the number of sections.
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    // Return the number of rows in the section.
    return 10;
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    CustomTableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"CustomTableViewCell" forIndexPath:indexPath];
    
    cell.delegate = self;
    cell.likeButton.tag = indexPath.row + 99;
    cell.customTitle.text = [NSString stringWithFormat:@"cell %ld", indexPath.row];
    
    // Configure the cell...
    
    return cell;
}

#pragma mark - CustomTableViewCellDelegate

- (void)likeButtonDidSelected:(CustomTableViewCell *)cell atIndex:(NSInteger)index {
    NSLog(@"%s, for Cell : %@, index : %ld" , __func__, cell, (long)index);
}

